<h2>Captioning Tool</h2>
<p>If <em>media tags</em> are specified in the Content tab, an instructor can make use of this Captioning Tool to create subtitles for the specified videos.</p> 
<p>One can click the "Subtitle Me" link present under each video.</p>
<p>A pop-up would be shown which would allow one to select options for the captioning process. </p>
<p>After following the instructions, the instructor would be able to generate the subtitle file for any video.</p>
<p>Please check out <a href="http://www.amara.org/en/">Amara website</a> for further instructions.</p>

