<h2>Media Interface</h2>
<p>If <em>media tags</em> were specified in the Content tab, various options like dimensions, captions, transcripts may be defined under the Media Interface tab. </p> 
<p>On clicking Insert/Change button, one can insert/change the various options by inserting files from the File Manager window that opens up.</p>
<p>Once all the options are specified, one should click the Update button and the new media tag would be generated.</p>
<p>Once done, choose the <kbd>Close after Saving</kbd> option, then press <kbd>Save</kbd></p>


